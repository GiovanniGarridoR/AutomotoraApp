    private void btn_mostrarautosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrarautosActionPerformed
          ControladorAuto cl = new ControladorAuto();
       ArrayList<Vehiculo> misvehiculos = cl.traerVehiculo();
       //obtener el modelo de la tabla
       DefaultTableModel modeloTabla = (DefaultTableModel) tbl_listavehiculo.getModel();

       //Dejamos el modelo sin registros
       modeloTabla.setRowCount(0);
       
       for(Vehiculo vh : misvehiculos)
       {
           String patente = vh.getPatente();
           String marca = vh.getMarca();
           int precio = vh.getPrecio();
           int anio_fabricacion = vh.getAnio_fabricacion();
           boolean es_nuevo = vh.isEs_nuevo();
          
           
           //crear registro del arraylist a la tabla 
           modeloTabla.addRow(new Object[]{patente,marca,anio_fabricacion,precio,es_nuevo});
        }

        
    }//GEN-LAST:event_btn_mostrarautosActionPerformed

